package com.ssafy.happyhouse.searchCI.model;

public class SearchCIModel {
    String shopName;
    String jibun_address;
    String longitude;
    String latitude;

    public String getShopName() {
        return shopName;
    }

    public void setShopName(String shopName) {
        this.shopName = shopName;
    }

    public String getJibun_address() {
        return jibun_address;
    }

    public void setJibun_address(String jibun_address) {
        this.jibun_address = jibun_address;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }
}
